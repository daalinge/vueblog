                        <h4 id="引言：">引言：</h4>
                        <p>2020年8月10号，从郑州抵达到深圳！！！</p>
                        <h4>始</h4>
                        <p style="text-indent: 2em;">不做长篇言论，直接入题。2020年8月9号，和前端几个同学决定一块出发到深圳，和张云鹏老师一块坐火车，晚上老师给我们上了一课，感觉似乎有点悬乎，但我们还是鼓着勇气决定去拼了一把。长达十几个小时的路程，晚上倒是有点疲惫 ，一觉睡到了天亮，最后还是因为车厢内有千年一遇的呼噜王 ，迫使我连懒觉真的睡不下去了，然后下床去看外边的风景。</p>
                        <hr>
                        <h4>中</h4>
                        <pre><code> ~~~~~长达十几个小时的路程终于到达了深圳，老师领着我们出去了之后做了好长时间的地铁，在地铁上，见到了南方各种各样plmm，来到了一线城市中，什么都没有感觉到，只感觉到了一股股的压力，在地铁中的人不一定都是高高兴兴的过来，高高兴兴的回去，在他们的眼里能够感觉得到，有一种压迫感，看着他们打着电话还笑嘻嘻的，这种笑嘻嘻的是那种开开心心的笑嘻嘻吗，谁也猜不准他是在应付着谁，也许是自己的客户，也许是自己的上司，更希望是对自己家人的笑嘻嘻。</code></pre>
                        <hr>
                        <h4>抵达</h4>
                        <blockquote>
                            <p style="text-indent: 2em;">做了好长时间的地铁，终于下来了，8月份的天气对于深圳还算可以，毕竟深圳是一个恒温的城市，每天都是这个温度，还是很不错的，不算太热，也不算太冷，拉着重重的行李箱，背着重重的鞋 ，还拖拉着一个拖鞋，这种天气走很长时间还是挺累挺热的，似乎感觉到了绝望，自己在大城市中打拼真的不是那么好拼的，当时心里一直在想奥利给，真的是没办法，终于老师带着我们五个来到了一个8人间的宿舍，还是那么的挤，房价还是那么的贵，刚来到这里，坐火车的400多块钱都是问家人要的，实在不想再去问家人要钱，但最后也是没办法，还是要了1500块钱交了房租，忽然有种想放弃的感觉，曾经和家人说过的去大城市发展，现在一开始却问家人要那么多的钱，实在于心不忍，住着这既拥挤又贵的宿舍，但又不得不住，也许这就是大城市吧，先苦后甜，也是一点点打拼而来的，谁一开始不是努力打拼着，心里无时无刻的想，不要急，慢慢的造，人有多大胆，定有多大产。加油！！！奥利给。</p>
                            <p  style="text-indent: 2em;">来到这里，生活用品什么都没有，都要去出去买，也是要花钱的喽，出去简简单单的买了一点东西，牙刷牙膏，盆等等，回去之后，今天算是很疲惫的一天，也是简简单单的把床铺了一下，倒在床上不那么一会就睡着了，毕竟第二天早上还要早起投递简历，找工作的风波就这样到来了</p>
                        </blockquote>
                        <hr>
                        <ol start="2">
                            <li>面试史
                                <blockquote>
                                    <p style="text-indent: 2em;">第二天早上，也就是8月11号，前天晚上老师说今天要在前程无忧投递上300份简历，这个东西也是第一次用 ，还有boss直聘，智联招聘，根本不知道各个公司是什么样的一个进展，我们在前程无忧做了一个筛选，把自己合适的职位筛选出来，好在这东西能一次投递50份简历，搜上三下，150份就这样投递出去了，虽然是筛选，但什么样的公司都有，不过一会，室友的电话开始响了起来，一个比一个忙，收到了各个公司的邀请面试，而我的电话还是一往平静，没有任何消息，前程无忧投递完之后，也去了boss直聘，boss直聘的是直接可以和各个公司的前台联系，前台感觉合适的话，就直接面试邀请，还是挺不错的，几个小时下来，他们都各自有了后一天的面试邀请，在吃饭的时候，一个公司好像注意到了我，但他们的效率是真的慢，等的我燥火，吃完饭之后，终于有一家公司给我打电话了，也就是后一天早上的面试，更巧的是，我和我的室友去了同一家公司，下午依然在联系，但效果几乎都不是太好。</p>
                                    <p style="text-indent: 2em;">第二天是上午10点的面试，我和我室友也不敢迟到，老早的起来了，那天还是下着雨，确实感觉到了一丝丝的心累，到了那地方之后，我和我室友都傻眼了，这个公司竟然是在一个居民楼上边，但是在云和的时候，张鹏浩说如果是居民楼的什么什么公司最好不要去，有可能是小黑屋哦，不过我们两个想 ，来都来了，就要去上面看一看，我问了一下张云鹏老师，他说的没有关系，可以去，我们也没有管那么多，直接就进去了，进去之后我们两个就傻眼了，这个公司里一共才见到3个人，真的是无语了，更懵逼的是，老板直接让我们俩上机操作，虽然我们俩是培训机构培训出来的，但实力还是不允许，就像老师之前说过的，虽然是培训出来的，但我们出去找工作真的是什么都不是，我现在终于相信这句话了</p>
                                    <p style="text-indent: 2em;">长达1个多小时的上机，真正感觉到了困难，在学习的时候那是写的什么项目，什么都不算，到了真正的实战，真的是看一脸懵逼，琢磨了好久，才算是写出来一个简单的渲染，但这点并不代表什么，老板根本不看在眼里，最后老板以委婉的接口让我们走了，说的是什么他们需要实力过硬的，我当时就想去你妹的吧，也不看看自己是什么公司，还要实力过硬的，垃圾的一批，我们两个下楼之后，外边突然下起了大雨，那是真的大，还好带了伞，天空灰蒙蒙的下着雨，我们两个的心情都不是太好，下午还有各自的一个面试，我们两个吃过饭之后就分开头去面试了，下午的是在南山区，在当时的龙岗区过去那边是真的远，也只好吃过饭直接打了公交过去，刚上去公交没多久，就又下起了大雨，在车上坐了大概将近两个小时，下车之后大雨还在下，只好一手拿手机导航，一手拿雨伞，生活不得不迫使我去坚持，到了楼下之后，问了一个人之后才知道这个公司的位置，乘电梯上去了，刚到里边，前台直接给我了一份面试题，感觉这才是真正的面试，上午的那家那是啥，面试题很简答，我一会写完了， 也有一两道难得，不过都让我百度了出来，等了好久之后，才有人让我去面试，第一次真正面试的我以为面试时多么严肃的一件事，然而没有什么好害怕的，我甚至一点紧张都没有，虽然是不害怕，但让这个人直接给我问懵逼了，毕竟自己是没有在公司经历过的，还有我们的简历也是造假出来的，不造假的话面试的机会是很少的，我们也只能搏一搏，假装自己是在公司待过一年的人，但我第一次正儿八经的面试，好像这个人看出了我不是真正的一年的经验，毕竟写项目根本不着边，随便说了几分钟后，这个人就直接说让我回去等通知，我知道一般这样的结果是不会有结果，但我还是自信满满，他爱要不要，我就当是经历了，仅仅面试了一天，我差不多已经找到了感觉，回去之后继续投递简历，等待邀请面试，第二天上午的面试我依然还是和我室友面试的同一家。</p>
                                </blockquote>
                            </li>
                        </ol>
                        <hr>
                        <h4>经验搏一搏</h4>
                        <blockquote>
                            <p style="text-indent: 2em;">犹如昨天晚上老师说的一样，对于在公司面试，要把自己的信息拿出来，没有过没关系，关键在于吸收经验。另一方面，既然都假装了，要假装出自己很牛逼的样子，这样才可能让面试的人信服你，哪怕你是真的不会，也要硬气的去说一说，错就错了，没有什么关系。</p>
                            <p style="text-indent: 2em;">我怀揣着信心，和室友来到了这一家公司，这家公司也是一二十人的样子，但工资给的也不是太高，我直接进去碰一碰运气，进来的这个人说话有点傻逼，眼睛还那么有点斜，他问我的问题也都是基础的，偶尔也会有那么一点难得，关于这点难得，我直接硬气的和他怼，他说我说的这个答案对吗，我说的对，其实这是不对的，但我还是这样去说，因为我不可能后退，不要让他以为自己很弱的样子，毕竟气场不能认输，和这个人对峙了几十分钟后，然后他们的人事来了，然后又哔哔了好几分钟，然后让我等通知，这次的面试我很舒畅，毕竟我的气场拿出来了，输赢倒是无所谓，然后我室友进去面试了，我在外面也是等了好长时间，最后他也出来了，感觉还都挺不错，因为我面试了一天之后，大概的道理和规矩也都差不多明白了，公司里边的怎么去面试一个人，我似乎也是明白了，这一家我感觉有70的几率，我把能说的都说了 ，气场这一块我也是拿的死死的，从不输于任何人，这一家算是结束了，然后准备吃饭去下一家，刚吃过饭，外面就又下起了大雨，似乎有点难，再难也要坚持下去，吃完饭看了一下手机，下一家离的也是太远了，就在这时，又有一家公司给我打电话，此时我的面试已经排到了下周的周一。</p>
                        </blockquote>
